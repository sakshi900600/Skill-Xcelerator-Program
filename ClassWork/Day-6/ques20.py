# Define a function make_icecream(flavor, scoops) that prints:

def make_icecream(flavor, scoops):
    res = f"Making {scoops} scoops of {flavor} ice cream 🍦🍦"
    return res




flavor = "Chocolate"
scoops = 6   

print(make_icecream(flavor, scoops))
# Output: Making 6 scoops of Chocolate ice cream 🍦🍦